package Qublog::Schema::Result::CommentTag;
use Moose;
extends qw( Qublog::Schema::Result );

=head1 NAME

Qublog::Schema::Result::CommentTag - links comments to tags

=head1 DESCRIPTION

This model links a comment to a tag.

=head1 SCHEMA

=head2 id

This is the autogenerated ID for the link.

=head2 comment

The L<Qublog::Schema::Result::Comment> linked.

=head2 tag

The L<Qublog::Schema::Result::Tag> linked.

=cut

__PACKAGE__->load_components(qw( Core ));
__PACKAGE__->table('comment_tags');
__PACKAGE__->add_columns(
    id      => { data_type => 'int' },
    comment => { data_type => 'int' },
    tag     => { data_type => 'int' },
);
__PACKAGE__->set_primary_key('id');
__PACKAGE__->belongs_to( comment => 'Qublog::Schema::Result::Comment' );
__PACKAGE__->belongs_to( tag => 'Qublog::Schema::Result::Tag' );

