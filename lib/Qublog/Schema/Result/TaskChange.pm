package Qublog::Schema::Result::TaskChange;
use Moose;
extends qw( Qublog::Schema::Result );

=head1 NAME

Qublog::Schema::Result::TaskChange - changes to a task associated with a task log

=head1 DESCRIPTION

These objects track the history of changes to values.

=head1 SCHEMA

=head2 id

The autogenerated ID field.

=head2 task_log

The L<Qublog::Schema::Result::TaskLog> this change belongs to.

=head2 name

The field that changed.

=head2 old_value

The value the field had before.

=head2 new_value

The new value the field has been assigned.

=cut

__PACKAGE__->load_components(qw( Core ));
__PACKAGE__->table('task_changes');
__PACKAGE__->add_columns(
    id        => { data_type => 'int' },
    task_log  => { data_type => 'int' },
    name      => { data_type => 'text' },
    old_value => { data_type => 'text' },
    new_value => { data_type => 'text' },
);
__PACKAGE__->set_primary_key('id');
__PACKAGE__->belongs_to( task_log => 'Qublog::Schema::Result::TaskLog' );

