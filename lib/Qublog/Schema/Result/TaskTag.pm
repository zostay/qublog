package Qublog::Schema::Result::TaskTag;
use Moose;
extends qw( Qublog::Schema::Result );

=head1 NAME

Qublog::Schema::Result::TaskTag - links tasks to tags

=head1 DESCRIPTION

Tags on tasks have two purposes. One is to categories the tasks, the other is to provide nicknames to the tasks.

=head1 SCHEMA

=head2 id

The autogenerated ID field.

=head2 task

The L<Qublog::Schema::Result::Task> being tagged.

=head2 tag

The L<Qublog::Schema::Result::Tag> tagging the task.

=head2 sticky

This boolean flag makes the tag sticky, which means that it cannot be removed from the task without confirmation. 

=head2 nickname

This boolean flag marks the tag as used a nickname, rather than a category for the task.

=cut

__PACKAGE__->load_components(qw( Core ));
__PACKAGE__->table('task_tags');
__PACKAGE__->add_columns(
    id       => { data_type => 'int' },
    task     => { data_type => 'int' },
    tag      => { data_type => 'int' },
    sticky   => { data_type => 'boolean' },
    nickname => { data_type => 'boolean' },
);
__PACKAGE__->set_primary_key('id');
__PACKAGE__->belongs_to( task => 'Qublog::Schema::Result::Task' );
__PACKAGE__->belongs_to( tag => 'Qublog::Schema::Result::Tag' );

=head1 METHODS

=head2 new

Sets the boolean values to the default of false.

=cut

sub new {
    my ($class, $attrs) = @_;

    $attrs->{sticky}   = 0 unless defined $attrs->{sticky};
    $attrs->{nickname} = 0 unless defined $attrs->{nickname};

    return $class->next::method($attrs);
}

